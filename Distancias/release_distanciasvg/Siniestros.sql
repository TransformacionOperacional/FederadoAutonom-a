        ON PR.PRODUCTO_ID = PL.PRODUCTO_ID

    INNER JOIN MDB_SEGUROS_COLOMBIA.V_RAMO AS R
        ON R.RAMO_ID = PR.RAMO_ID

    INNER JOIN MDB_SEGUROS_COLOMBIA.V_SUCURSAL AS SUC
        ON P.SUCURSAL_ID = SUC.SUCURSAL_ID

    INNER JOIN MDB_SEGUROS_COLOMBIA.V_CANAL_COMERCIAL AS CANAL
        ON CANAL.CANAL_COMERCIAL_ID = SUC.CANAL_COMERCIAL_ID

    INNER JOIN MDB_SEGUROS_COLOMBIA.V_GRUPO_CANAL_COMERCIAL AS GPC
        ON CANAL.GRUPO_CANAL_COMERCIAL_ID = GPC.GRUPO_CANAL_COMERCIAL_ID

    WHERE R.RAMO_ID = 274
      AND CANAL.CANAL_COMERCIAL_ID IN (
          24390656,
          28686321,
          24390667,
          48676970,
          24390669
      )
)
WITH DATA
PRIMARY INDEX (NUMERO_POLIZA, POLIZA_ID)
ON COMMIT PRESERVE ROWS;


